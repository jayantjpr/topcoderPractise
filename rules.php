<h2 style="text-align:center">Rules</h2>
<div id="rules">
    <br/>
    <p>
        Each mock contest will consist of solving some problems from the TopCoder archive in a fixed time interval.
        To make your practice useful, here are the rules of the mock contest.
        All the rules won't make sense right now &mdash;
        once you start your first mock contest, you will be able to understand them better!
    </p>

    <br/>
    <p>
    <h4>Before the contest</h4>
    <ol>
        <li>
            The participants first log into the site using their TopCoder Handle as username and password which they provided during the registration.
        </li>
        <li>
            Once you login you would see the the list of all competitions along with its starting and ending times.
        </li>
        <li>
            The link to any mock contest will ONLY be active when the contest goes live.
        </li>
        <li>
            The link to any contest would take you to a page where all the questions of the contest are listed. These problems are to be solved in the topcoder arena.
        </li>
    </ol>
    </p>

    <br/>
    <p>
    <h4>During The contest</h4>
    <ol>
        <li>
        Spend the first 2 minutes to clear your past data from the TopCoder arena &mdash;
        for each problem in the contest, go the practice room and clear your data for that problem.
        <code>Menu -> Practice Options -> Clear Problem(s)</code>
        This is important to correctly know your practice performance.
        </li>
        <li>
        You can start opening problems once you've cleared ALL your contest data.
        You MUST run system tests as soon as you submit a problem.
        <code>Menu -> Practice Options -> Run System Test</code>
        </li>
        <li>
        AFTER your code passes all tests, click the button marked <input type="button"  value="I pass system test" class = "passed"> against the question on this site.
        Suppose you wrongly press the button, don't sweat!
        Simply go the arena and clear your data for that problem. Otherwise, it's cheating <code>:|</code>
        </li>
        <li>
        You are not allowed to use any pre-written code, including libraries, functions and other code snippets that you may have written yourself.
        All code you submit must be written by you during the mock contest.
        </li>
        <li>
        It is very easy to make fake score entries, re-use older code etc. Have the good sense to not cheat yourself &mdash; because you're not fooling anybody else!
        </li>
    </ol>
    </p>

    <br/>
    <p>
    <h4>After The contest</h4>
    <ol>
        <li>
        Once the contest is over, a leadboard would be visible on the contest page.
        </li>
        <li>
        Also, the button <input type="button"  value="I pass system test" class = "passed" /> would become invisible.
        </li>
        <li>
        Ranks would be given based on the number of problems solved and ties would be broken based on the Topcoder Practice Room score.
        </li>
    </ol>
    </p>    
